#pragma once
#ifndef _DES_PC1_H_
#define _DES_PC1_H_

#include <stdint.h>

/**
 * Permuted Choice 1 (PC-1) - First half
 *
 * Input: 64 bits.
 * Output: 28 bits.
 */
static inline uint32_t DES_PC1_C(uint64_t in) {
    uint32_t C = 0;
    C |= (in & 0x80) << 20;
    C |= (in & 0x8000) << 11;
    C |= (in & 0x800000) << 2;
    C |= (in & 0x80000000) >> 7;
    C |= (in & 0x8000000000) >> 16;
    C |= (in & 0x800000000000) >> 25;
    C |= (in & 0x80000000000000) >> 34;
    C |= (in & 0x8000000000000000) >> 43;
    C |= (in & 0x40) << 13;
    C |= (in & 0x4000) << 4;
    C |= (in & 0x400000) >> 5;
    C |= (in & 0x40000000) >> 14;
    C |= (in & 0x4000000000) >> 23;
    C |= (in & 0x400000000000) >> 32;
    C |= (in & 0x40000000000000) >> 41;
    C |= (in & 0x4000000000000000) >> 50;
    C |= (in & 0x20) << 6;
    C |= (in & 0x2000) >> 3;
    C |= (in & 0x200000) >> 12;
    C |= (in & 0x20000000) >> 21;
    C |= (in & 0x2000000000) >> 30;
    C |= (in & 0x200000000000) >> 39;
    C |= (in & 0x20000000000000) >> 48;
    C |= (in & 0x2000000000000000) >> 57;
    C |= (in & 0x10) >> 1;
    C |= (in & 0x1000) >> 10;
    C |= (in & 0x100000) >> 19;
    C |= (in & 0x10000000) >> 28;
    return C;
}

/**
 * Permuted Choice 1 (PC-1) - Second half
 *
 * Input: 64 bits.
 * Output: 28 bits.
 */
static inline uint32_t DES_PC1_D(uint64_t in) {
    uint32_t D = 0;
    D |= (in & 0x2) << 26;
    D |= (in & 0x200) << 17;
    D |= (in & 0x20000) << 8;
    D |= (in & 0x2000000) >> 1;
    D |= (in & 0x200000000) >> 10;
    D |= (in & 0x20000000000) >> 19;
    D |= (in & 0x2000000000000) >> 28;
    D |= (in & 0x200000000000000) >> 37;
    D |= (in & 0x4) << 17;
    D |= (in & 0x400) << 8;
    D |= (in & 0x40000) >> 1;
    D |= (in & 0x4000000) >> 10;
    D |= (in & 0x400000000) >> 19;
    D |= (in & 0x40000000000) >> 28;
    D |= (in & 0x4000000000000) >> 37;
    D |= (in & 0x400000000000000) >> 46;
    D |= (in & 0x8) << 8;
    D |= (in & 0x800) >> 1;
    D |= (in & 0x80000) >> 10;
    D |= (in & 0x8000000) >> 19;
    D |= (in & 0x800000000) >> 28;
    D |= (in & 0x80000000000) >> 37;
    D |= (in & 0x8000000000000) >> 46;
    D |= (in & 0x800000000000000) >> 55;
    D |= (in & 0x1000000000) >> 33;
    D |= (in & 0x100000000000) >> 42;
    D |= (in & 0x10000000000000) >> 51;
    D |= (in & 0x1000000000000000) >> 60;
    return D;
}

#endif // !_DES_PC1_H_
