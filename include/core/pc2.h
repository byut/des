#pragma once
#ifndef _DES_PC2_H_
#define _DES_PC2_H_

#include <stdint.h>

/**
 * Permuted Choice 2 (PC-2)
 *
 * Input: 56 bits
 * Output: 48 bits
 */
static inline uint64_t DES_PC2(uint64_t C, uint64_t D) {
    uint64_t K = 0;
    K |= (C & 0x4000) << 33;
    K |= (C & 0x800) << 35;
    K |= (C & 0x20000) << 28;
    K |= (C & 0x10) << 40;
    K |= (C & 0x8000000) << 16;
    K |= (C & 0x800000) << 19;
    K |= (C & 0x2000000) << 16;
    K |= (C & 0x1) << 40;
    K |= (C & 0x2000) << 26;
    K |= (C & 0x400000) << 16;
    K |= (C & 0x80) << 30;
    K |= (C & 0x40000) << 18;
    K |= (C & 0x20) << 30;
    K |= (C & 0x200) << 25;
    K |= (C & 0x10000) << 17;
    K |= (C & 0x1000000) << 8;
    K |= (C & 0x4) << 29;
    K |= (C & 0x100000) << 10;
    K |= (C & 0x1000) << 17;
    K |= (C & 0x200000) << 7;
    K |= (C & 0x2) << 26;
    K |= (C & 0x100) << 18;
    K |= (C & 0x8000) << 10;
    K |= (C & 0x4000000) >> 2;
    K |= (D & 0x8000) << 8;
    K |= (D & 0x10) << 18;
    K |= (D & 0x2000000) >> 4;
    K |= (D & 0x80000) << 1;
    K |= (D & 0x200) << 10;
    K |= (D & 0x2) << 17;
    K |= (D & 0x4000000) >> 9;
    K |= (D & 0x10000);
    K |= (D & 0x20) << 10;
    K |= (D & 0x800) << 3;
    K |= (D & 0x800000) >> 10;
    K |= (D & 0x100) << 4;
    K |= (D & 0x1000) >> 1;
    K |= (D & 0x80) << 3;
    K |= (D & 0x20000) >> 8;
    K |= (D & 0x1) << 8;
    K |= (D & 0x400000) >> 15;
    K |= (D & 0x8) << 3;
    K |= (D & 0x400) >> 5;
    K |= (D & 0x4000) >> 10;
    K |= (D & 0x40) >> 3;
    K |= (D & 0x100000) >> 18;
    K |= (D & 0x8000000) >> 26;
    K |= (D & 0x1000000) >> 24;
    return K;
}

#endif // !_DES_PC2_H_
